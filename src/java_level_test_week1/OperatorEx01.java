package java_level_test_week1;

public class OperatorEx01 {
    public static void main(String[] args) {
        int a = 3;

        a = a + 1;
        System.out.println(a);

        a += 1;
        System.out.println(a);

        a -= 1;
        System.out.println(a);

        a *= 2;
        System.out.println(a);

        a /= 2;
        System.out.println(a);

        a %= 2;
        System.out.println(a);
    }
}
